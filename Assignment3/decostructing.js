// Object Destructuring
let person = { name: "John", age: 30, city: "New York" };
let { name, age, city } = person;
console.log(name); 
console.log(age); 
console.log(city); 

// Array Destructuring
let fruits = ["apple", "banana", "mango"];
let [first, second, third] = fruits;
console.log(first); 
console.log(second); 
console.log(third); 